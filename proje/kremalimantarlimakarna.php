        <h2>Kremalı Mantarlı Makarna Tarifi'nin Malzemeleri</h2>
        <ul>
            <li>300 gram makarna (penne, fettuccine veya tagliatelle gibi)</li>
            <li>200 gram mantar (isteğe bağlı olarak farklı mantar türleri kullanılabilir)</li>
            <li>1 adet soğan (ince doğranmış)</li>
            <li>2 diş sarımsak (ezilmiş)</li>
            <li>1 su bardağı krema (200 ml)</li>
            <li>1/2 su bardağı rendelenmiş parmesan peyniri</li>
            <li>2 yemek kaşığı tereyağı veya zeytinyağı</li>
            <li>Tuz ve karabiber</li>
            <li>1/2 çay kaşığı kuru kekik veya taze kekik (isteğe bağlı)</li>
            <li>1/2 çay kaşığı kırmızı biber (isteğe bağlı)</li>
            <li>Taze doğranmış maydanoz (servis için)</li>
        </ul>

        <h2>Kremalı Mantarlı Makarna Tarifi'nin Yapılışı</h2>
        <ul>
            <li><h3>Makarna Hazırlığı:</h3>
                Büyük bir tencerede bol tuzlu suyu kaynatın. Makarnayı ekleyin ve paketin üzerindeki talimatlara göre al dente kıvamına gelene kadar pişirin. Piştikten sonra suyunu süzün ve kenarda bekletin.</li>
           <br>
                <li><h3>Mantar ve Soğan Karışımı:</h3>
                Büyük bir tavada tereyağını veya zeytinyağını orta ateşte ısıtın. İnce doğranmış soğanları ekleyin ve yumuşayana kadar kavurun.
                Ezilmiş sarımsakları ekleyin ve kokusu çıkana kadar (yaklaşık 1 dakika) kavurun.
                Dilimlenmiş mantarları ekleyin ve mantarlar suyunu salıp tekrar çekene kadar (yaklaşık 5-7 dakika) pişirin.</li>
            <br>
                <li><h3>Sosun Hazırlığı:</h3>
                Kremayı ekleyin ve sosu hafifçe kaynatın. Sos hafifçe koyulaşana kadar birkaç dakika pişirin.
                Rendelenmiş parmesan peynirini ekleyin ve karıştırarak peynirin erimesini sağlayın.
                Kuru kekik ve kırmızı biberi ekleyin. Tuz ve karabiberle tatlandırın. Sosun tadını kontrol edin ve gerektiğinde baharatları ayarlayın.</li>
            <br>
                <li><h3>Makarna ve Sosun Buluşması:</h3>
                Pişmiş makarnayı tavadaki sosun içine ekleyin ve iyice karıştırarak makarnanın sosla iyice kaplanmasını sağlayın. Eğer sos çok koyu ise, makarna haşlama suyundan birkaç yemek kaşığı ekleyerek kıvamını ayarlayabilirsiniz.</li>
            <br>
                <li><h3>Servis:</h3>
                Kremalı mantarlı makarnayı sıcak olarak servis tabaklarına alın.
                Üzerine taze doğranmış maydanoz serpiştirerek servis edin.
                İsteğe bağlı olarak ekstra parmesan peyniri de serpebilirsiniz.</li>
         
