        <h2>Su Böğreği Tarifi'nin Malzemeleri</h2>
        <ul>
            <li>6 su bardağı un</li>
            <li>5 adet yumurta</li>
            <li>1 çay bardağı su</li>
            <li>1 tatlı kaşığı tuz</li>
        </ul>

        <h2>İç harcı için;</h2>
        <ul>
            <li>500 gram beyaz peynir veya lor peyniri</li>
            <li>1 demet maydanoz (ince doğranmış)</li>
            <li>2 yemek kaşığı zeytinyağı</li>
            <li>200 gram eritilmiş tereyağı veya margarin</li>
            <li>1 tatlı kaşığı tuz (haşlama suyu için)</li>

        </ul>

        <h2>Su Böreği Tarifi'nin Yapılışı</h2>
        <ul>
            <li><h3>1)Hamurun Hazırlanması:</h3>
                Unu geniş bir kaba eleyin ve ortasını havuz şeklinde açın.
                Yumurta, su ve tuzu ekleyerek yoğurun.
                Pürüzsüz bir hamur elde edene kadar yoğurmaya devam edin.
                Hamuru 10 eşit parçaya bölün ve bezeler haline getirin.
                Bezeleri nemli bir bezle örtüp 30 dakika dinlendirin.</li>
           <br>
                <li><h3>2)İç Harcın Hazırlanması:</h3>
                Beyaz peynir veya lor peynirini ufalayın.
                İnce doğranmış maydanozu ekleyin ve karıştırın.</li>
            <br>
                <li><h3>3)Hamurun Açılması:</h3>
                Dinlenen bezeleri un yardımıyla ince yufkalar halinde açın.
                Açılan yufkaları temiz bir bez üzerine serin.</li>
            <br>
                <li><h3>4)Yufkaların Haşlanması:</h3>
                Geniş bir tencereye su koyun ve kaynatın. İçine tuz ve zeytinyağı ekleyin.
                Başka bir kaba soğuk su hazırlayın.
                Yufkaları teker teker kaynar suya atın ve 1-2 dakika haşlayın.
                Haşlanan yufkaları hemen soğuk suya alın ve ardından temiz bir bezle kurulayın.
                    </li>
            <br>
                <li><h3>5)Böreğin Hazırlanması:</h3>
                Eritilmiş tereyağı veya margarini bir kaseye alın.
                Börek tepsisini yağlayın.
                İlk yufkayı kuru olarak tepsiye serin (bu taban yufkası haşlanmayacak).
                Her kat yufkanın üzerine eritilmiş tereyağı gezdirin ve peynirli harçtan serpiştirin.
                Bu işlemi yufkalar bitene kadar tekrarlayın. (Son yufka da kuru olacak.)</li>
            <br>
            <li><h3>6)Pişirme:</h3>
            Önceden ısıtılmış 180 derece fırında yaklaşık 45-50 dakika, üzeri kızarana kadar pişirin.
            Böreği fırından aldıktan sonra 10-15 dakika dinlendirin.
            Dilimleyerek servis yapın</li>
            <br><br>
            <h3>AFİYET OLSUN!</h3>
